    private void BeratLaundryKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_BeratLaundryKeyTyped
char c = evt.getKeyChar();
    String text = BeratLaundry.getText();
    
    // Hanya izinkan angka, backspace, delete, dan satu titik desimal
    if (!Character.isDigit(c) && c != KeyEvent.VK_BACK_SPACE && c != KeyEvent.VK_DELETE && c != '.') {
        evt.consume();  // Mengabaikan karakter yang tidak valid
    }
    
    // Izinkan hanya satu titik desimal
    if (c == '.' && text.contains(".")) {
        evt.consume();  // Mengabaikan titik desimal tambahan
    }        // TODO add your handling code here:
    }//GEN-LAST:event_BeratLaundryKeyTyped
