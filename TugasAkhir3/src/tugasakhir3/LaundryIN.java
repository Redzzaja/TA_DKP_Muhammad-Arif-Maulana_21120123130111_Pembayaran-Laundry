    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
    String selectedDay = (String) jComboBox1.getSelectedItem();
        String weightText = BeratLaundry.getText();
        boolean isSepraiChecked = jCheckBox2.isSelected();
        boolean isSelimutChecked = jCheckBox1.isSelected();
        Date tanggal = jDateChooser1.getDate();

        if (weightText.isEmpty()  || tanggal == null) {
            JOptionPane.showMessageDialog(null,"Lengkapi dulu bos ");
            return;
        }

        try {
            double weight = Double.parseDouble(weightText);
            double costPerKg;
            int daysToAdd;

            if ("2 Hari".equals(selectedDay)) {
                costPerKg = 5000;
                daysToAdd = 2;
            } else if ("3 Hari".equals(selectedDay)) {
                costPerKg = 4000;
                daysToAdd = 3;
            } else if ("Express 1 Hari".equals(selectedDay)) {
                costPerKg = 8000;
                daysToAdd = 1;
            } else {
                CekHarga.setText("Total biaya: ");
                return;
            }

            double totalCost = costPerKg * weight;

            if (isSepraiChecked) {
                totalCost += 10000;
            }
            if (isSelimutChecked) {
                totalCost += 15000;
            }

            String biaya = "Total biaya: Rp. " + totalCost;
            
            LocalDate tanggalSaatIni = tanggal.toInstant().atZone(ZoneId.systemDefault()).toLocalDate();
            LocalDate tanggalPengambilan = tanggalSaatIni.plusDays(daysToAdd);
            Date tanggalPengambilanDate = Date.from(tanggalPengambilan.atStartOfDay(ZoneId.systemDefault()).toInstant());

            SimpleDateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
            String tanggalPengambilanStr = dateFormat.format(tanggalPengambilanDate);
            String combinedInfo = "Tanggal Selesai: " + tanggalPengambilanStr + "\r\n" + "Berat: " + weightText + " Kg " + "\r\n" + biaya;

            
            CekHarga.setText(combinedInfo);
            isHargaTervalidasi = true;
             } catch (NumberFormatException e) {
            CekHarga.setText("Total biaya: ");
        }    // TODO add your handling code here:
    }//GEN-LAST:event_jButton2ActionPerformed

